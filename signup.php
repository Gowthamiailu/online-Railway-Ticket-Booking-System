                    <input type="password" id="password" name="password" required class="w-full px-3 py-2 border rounded-lg">
                </div>
                <button type="submit" class="w-full bg-green-500 text-white font-bold py-2 px-4 rounded-lg hover:bg-green-600">Sign Up</button>
